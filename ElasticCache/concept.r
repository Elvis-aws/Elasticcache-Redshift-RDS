AWS ElastiCache
***************
    - AWS ElastiCache helps deploy and run Memcached or Redis protocol-compliant cache clusters in the cloud 
      easily.
    - ElastiCache is available in two flavours: Memcached and Redis
    - Helps make applications stateless by putting its state in elastic cache (Storing User session)
    - ElastiCache provides in-memory caching which can
        - Significantly lower latency and improve throughput
        - Read-heavy application workloads e.g. social networking, gaming, media sharing and Q&A portals or
          compute-intensive workloads such as a recommendation engine
        - Improve application performance by storing critical pieces of data in memory for low-latency access.
        - ElastiCache currently allows access only from the EC2 network and cannot be accessed from outside networks 
         like on-premises servers.