
***************************
Relational Database Service
***************************
    - Relational Database Service (RDS) is a web service that uses SQL as a query language
    - RDS features & benefits
        - We can not SSH into these DB instances 
        - Security group controls the access to a DB instance
        - Manages backups, software patching, automatic failure detection, and recovery.
        - Provides high availability with a primary instance and a synchronous standby secondary instance that 
        can be failover to seamlessly when a problem occurs.
        - Provides elasticity & scalability by enabling Read Replicas to increase read scaling.
        - Supports IAM users and permissions to control who has access to the RDS database service
        - Supports 
            MySQL, 
            MariaDB, 
            PostgreSQL, 
            Oracle, 
            Microsoft SQL Server, 
            Aurora 


    